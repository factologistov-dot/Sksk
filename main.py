active_tasks = {}

def get_mention(user_id):
    """Невидимая ссылка на человека по его ID"""
    return f"[\u200b](tg://user?id={user_id}){user_id}"

@client.on(events.NewMessage(outgoing=True, pattern=r'^\.menu'))
async def menu(event):
    start_time = time.time()
    await event.edit("🇦🇫 **жди** 🇦🇫")
    end_time = time.time()
    
    ping = round((end_time - start_time) * 1000)
    me = await client.get_me()
    
    text = (
        f"**🇦🇫 Kenoma And Kenosis 🇦🇫**\n"
        f"--- --- ---\n"
        f"🇦🇫 **Твой ID:** `{me.id}`\n 🇦🇫"
        f"🇦🇫 **ID чата:** `{event.chat_id}`\n 🇦🇫"
        f"🇦🇫 **Пинг:** `{ping}ms`\n 🇦🇫"
        f"🇦🇫 **Активно целей:** `{len(active_tasks)}` \n 🇦🇫"
        f"--- --- ---\n"
        f"🇦🇫 `.kk [id] [сек] [шапка]` (ответом на медиа для спама файлом)\n 🇦🇫"
        f"🇦🇫 `.k [id]` — остановить 🇦🇫"
    )
    await event.edit(text)

@client.on(events.NewMessage(outgoing=True, pattern=r'^\.kk (\d+) (\d+)(.*)'))
async def start_auto(event):
    user_id = int(event.pattern_match.group(1))
    delay = int(event.pattern_match.group(2))
    header = event.pattern_match.group(3).strip()
    chat_id = event.chat_id
    
    # Сохраняем медиа, если команда была ответом на фото/видео
    reply_to_media = await event.get_reply_message()
    
    if user_id in active_tasks:
        return await event.edit(f"🇦🇫 Уже запущено для `{user_id}` 🇦🇫")

    await event.edit(f"🇦🇫 **Автоответ запущен!**\nЦель: {get_mention(user_id)}\nИнтервал: {delay}с.🇦🇫")

    async def worker():
        idx = 0
        try:
            while True:
                # Ищем последнее сообщение от цели в этом чате, чтобы ответить на него
                messages = await client.get_messages(chat_id, limit=20)
                target_msg = None
                for m in messages:
                    if m.sender_id == user_id:
                        target_msg = m
                        break
                
                # Если сообщений от цели нет, отвечаем на саму команду или последнее сообщение в чате
                reply_id = target_msg.id if target_msg else event.id
                
                phrase = Shablon[idx % len(Shablon)]
                mention = get_mention(user_id)
                final_text = f"**{header}**\n\n{phrase}\n\nID: {mention}" if header else f"{phrase}\n\nID: {mention}"
                
                if reply_to_media and (reply_to_media.photo or reply_to_media.video):
                    # Отправляем медиа как ответ (reply)
                    await client.send_file(chat_id, reply_to_media.media, caption=final_text, reply_to=reply_id)
                else:
                    # Отправляем текст как ответ (reply)
                    await client.send_message(chat_id, final_text, reply_to=reply_id)
                
                idx += 1
                await asyncio.sleep(delay)
        except asyncio.CancelledError:
            pass
        except Exception as e:
            print(f"Ошибка в воркере: {e}")

    task = asyncio.create_task(worker())
    active_tasks[user_id] = task

@client.on(events.NewMessage(outgoing=True, pattern=r'^\.k (\d+)'))
async def stop_auto(event):
    user_id = int(event.pattern_match.group(1))
    if user_id in active_tasks:
        active_tasks[user_id].cancel()
        del active_tasks[user_id]
        await event.edit(f"🇦🇫 Автоответ для {get_mention(user_id)} **выключен**. 🇦🇫")
    else:
        await event.edit("🇦🇫 ID не найден в активных задачах. 🇦🇫")

